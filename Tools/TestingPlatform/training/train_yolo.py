from ultralytics import YOLO

# Завантажуємо попередньо натреновану модель Nano
# Для YOLOv8 використовуйте 'yolov8n.pt', для YOLO11 - 'yolo11n.pt'
model = YOLO('yolov8n.pt') 

# Запуск тренування
results = model.train(
    data='data.yaml',
    epochs=100,           # Для 1000 зображень 100 епох зазвичай достатньо для старту
    imgsz=640,            # Базовий розмір зображення (можна зменшити до 416, якщо не вистачає пам'яті)
    batch=8,              # Зменшіть до 4 або 2, якщо відеокарта має мало VRAM
    device='',            # Залиште порожнім для автовибору GPU. Якщо пишете 'cpu' - буде дуже довго.
    workers=2             # Кількість потоків для завантаження даних (зменшіть, якщо CPU слабкий)
)

print("Тренування завершено. Ваги збережено у папці runs/detect/train/weights/best.pt")